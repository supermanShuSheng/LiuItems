public class CCCCDDDD extends AAAABBB{

    private String abc;

    public String getAbc() {
        return abc;
    }

    public void setAbc(String abc) {
        this.abc = abc;
    }


    @Override
    public String toString() {
        return "CCCCDDDD{" +
                "abc='" + abc + '\'' +
                '}';
    }

    public CCCCDDDD(String abc) {
        this.abc = abc;
    }
}
